    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        String firstname = txtFname.getText();
        String lastname = txtLname.getText();
        String comment = txtComment.getText();
        String rating = txtRating.getText();

        if (firstname.isEmpty() || lastname.isEmpty() || comment.isEmpty() || rating.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields must be filled out", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            addRecord(firstname, lastname, comment, rating);
            JOptionPane.showMessageDialog(this, "Thanks for filling out our review. Here is a discount code that \n you can use to get discount on your tuition fee for next year.\n The code is : 8267. Head over to the vouchers page to claim this discount.", "Success", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSubmitActionPerformed


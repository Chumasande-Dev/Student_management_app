    private void addRecord(String firstname, String lastname, String comment, String rating) {
        
        String insertQuery = "INSERT INTO GuestReviews (GuestName, Rating, Comment) VALUES (?, ?, ?)";
        try (java.sql.Connection connection = DriverManager.getConnection("jdbc:sqlite:C:\\Users\\Louis\\Documents\\NetBeansProjects\\java-swing-slideshow-animation-main\\WILDatabase"); PreparedStatement statement = connection.prepareStatement(insertQuery)) {

            statement.setString(1, firstname + " " + lastname);
            statement.setString(2, comment);
            statement.setString(3, rating);

            statement.executeUpdate();

        } catch (SQLException e) {
            System.err.println("SQL Error: " + e.getMessage());
            e.printStackTrace();

        }
    }

